<?php
//     Пользователь вводит названия городов через пробел. Переставьте названия так,
//    чтобы названия были упорядочены по алфавиту.

// Получаем ввод пользователя
echo "Введите названия городов через пробел: ";
$input = trim(fgets(STDIN));

// Разбиваем введенную строку на массив городов
$cityArray = explode(' ', $input);

// Сортируем массив городов по алфавиту
sort($cityArray);

// Соединяем отсортированные города в строку с пробелами
$sortedCities = implode(' ', $cityArray);

// Выводим результат
echo "Отсортированные города: $sortedCities";
