<?php
//    Дана строка с цифрами, например: '12345678'. Просуммируйте числа этой строки вот таким образом: 12+34+56+78.
//    Строка, конечно же, может быть произвольной (но только с цифрами внутри)


// Исходная строка с цифрами
    $str = '12345678';

// Длина каждого числа (в данном случае, 2)
    $length = 2;

// Переменная для хранения суммы
    $sum = 0;

// Разбиваем строку на подстроки заданной длины
    $chunks = str_split($str, $length);

// Проходим по каждой подстроке и суммируем числа
    foreach ($chunks as $chunk) {
        $sum += intval($chunk);
    }

// Выводим результат
    echo "Сумма чисел: " . $sum . PHP_EOL;


