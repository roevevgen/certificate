<?php
//    Дан массив с числами. Запишите в новый массив только те числа, в которых есть цифра 5.


// Дан массив с числами
    $array = array(12, 35, 47, 58, 69, 75, 81, 94);
// Создаем функцию, которая проверяет, есть ли в числе цифра 5
    function has_digit_5($num)
    {
        // Преобразуем число в строку
        $str = strval($num);
        // Ищем в строке символ '5'
        return strpos($str, '5') !== false;
    }

// Фильтруем массив с помощью нашей функции
    $new_array = array_filter($array, 'has_digit_5');
// Выводим результат
    print_r($new_array);

