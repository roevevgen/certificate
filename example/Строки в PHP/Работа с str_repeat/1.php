    $rows = 9; // Количество рядов пирамиды

    for ($i = 1; $i <= $rows; $i++) {
        $spaces = str_repeat(' ', $rows - $i); // Добавляем пробелы перед символами 'x'
        $stars = str_repeat('x', $i) . "<br>"; // Строка из символов 'x'
        echo $spaces . $stars . PHP_EOL; // Выводим строку с переносом строки
    }


