<?php
//    Для решения задач данного блока вам понадобятся следующие функции: str_repeat Нарисуйте пирамиду, как показано на
//    рисунке, только у вашей пирамиды должно быть 9 рядов, а не 5. Решите задачу с помощью одного цикла и функции
//    str_repeat.
//    x
